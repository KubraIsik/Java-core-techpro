package day2_datatypes_concat_operators;

public class Variables02 {

    public static void main(String[] args) {
        // non-primitive data types
        // this type of data also class
        // created every class are also a data type (there are unlimited number of non-primitive data types)
        // String is a non-primitve data type and also a class
        // For non-primitives, Java allocates memory space of varying sizes depending on their size.

        String studentName = "Alican";

        String cityName = "İstanbul";
        System.out.println(cityName);

        cityName = "İzmir";
        System.out.println(cityName);
    }
}
